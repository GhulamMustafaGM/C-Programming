// Using a variable

#include <iostream>
using namespace std;

int main()
{
    int x;    // this declares a variable
    x = 1023; // this assigns 1023 to x

    cout << "\nThis program prints the values of x: ";
    cout << x << endl; // This displays 1023

    return 0;
}

/* Output
This program prints the values of x: 1023
*/
